#LESSON 29: WHAT IF
people = 20
cats = 30
dogs = 15

if people < cats
	puts "Too many cats"
end

if people > cats
	puts "Not many cats"
end

if people < dogs
	puts "More dogs"
end

if people > dogs
	puts "More people than dogs"
end

dogs += 5

if people >= dogs
	puts "People greater than or equal to dogs"
end

if people <= dogs
	puts "People less than equal to dogs"
end

if people == dogs
	puts "People are dogs"
end