tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

#Triple quotes allows you to put as many lines of text as you want until you type """ again
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat

# \\ 	Backslash ()
# \' 	Single-quote (')
# \" 	Double-quote (")
# \a 	ASCII bell (BEL)
# \b 	ASCII backspace (BS)
# \f 	ASCII formfeed (FF)
# \n 	ASCII linefeed (LF)
# \r 	ASCII Carriage Return (CR)
# \t 	ASCII Horizontal Tab (TAB)
# \uxxxx 	Character with 16-bit hex value xxxx (Unicode only)
# \v 	ASCII vertical tab (VT)
# \ooo 	Character with octal value ooo
